package com.iss.vo.param;

public class RelationshipNetbar {
	private String netbar_code;
	private String main_id;
	private String netbar_name;
	private String district_code;
	private String reg_address_detail;
	private String legalname;
	
	
	public String getNetbar_code() {
		return netbar_code;
	}
	public void setNetbar_code(String netbar_code) {
		this.netbar_code = netbar_code;
	}
	public String getMain_id() {
		return main_id;
	}
	public void setMain_id(String main_id) {
		this.main_id = main_id;
	}
	public String getNetbar_name() {
		return netbar_name;
	}
	public void setNetbar_name(String netbar_name) {
		this.netbar_name = netbar_name;
	}
	public String getDistrict_code() {
		return district_code;
	}
	public void setDistrict_code(String district_code) {
		this.district_code = district_code;
	}
	public String getReg_address_detail() {
		return reg_address_detail;
	}
	public void setReg_address_detail(String reg_address_detail) {
		this.reg_address_detail = reg_address_detail;
	}
	public String getLegalname() {
		return legalname;
	}
	public void setLegalname(String legalname) {
		this.legalname = legalname;
	}
	
	

}
